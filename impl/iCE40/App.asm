my_vector_table:
RESET_VEC:
	ba start
HSYNC_VEC:
	ba hs_handler
VSYNC_VEC:
	ba vs_handler
AUDIO_VEC:
	ba dummy_handler
SPI_FLASH:
	ba dummy_handler
GPIO_VEC:
	ba dummy_handler
VECTORS:
	.times 20 dw 0x0000

start:
		// Copy vectors
		mov r1, r0
		mov r2, my_vector_table
		mov r3, 32
copy_loop:
		ld r4,[r2, 0]
		st [r1, 0], r4
		add r1, 1
		add r2, 1
		sub r3, 1
		bnz copy_loop

// Set up a sprite

		mov r4, GFX_PAL + 1
		mov r1, pacman_palette
		mov r2, 15
pal_loop:
		ld r3, [r1, 0]
		add r1,1
		out [r4, 0], r3
		add r4, 1
		sub r2, 1
		bnz pal_loop

		mov r2, 320 | 1<<10 | 1<<15
		out [r0, SPRITE0_X], r2
		mov r2, 15<<10 | 240
		out [r0, SPRITE0_Y], r2
		mov r2, 256
		out [r0, SPRITE0_H], r2
		mov r2, 0x8000 	
		out [r0, SPRITE0_A], r2

		mov r4, 3
		out [r0, PWM_RED], r4
		mov r4, 1
		out [r0, PWM_GREEN], r4
		mov r4, 1
		out [r0, PWM_BLUE], r4

		// Fill audio buffers with saw tooth wave
		// octave higher in left channel

		mov r2, 512
		mov r3, 0	// index
		mov r4, 0	// saw value
saw_loop:
		out [r3, AUDIO_LEFT_MEM], r4
		mov r5, r4
		lsl r5
		out [r3, AUDIO_RIGHT_MEM], r5
		add r3, 1
		add r4, 4
		sub r2, 1
		bnz saw_loop

		// Set to run
		mov r1, 1
		out [r0, AUDIO_CONTROL], r1
	


		mov r2, banner
loop:
		ld r1, [r2, 0]
		add r2, 1
		or r1, r1
		bz die 
		out [r0, UART_TX_REG], r1
test_loop:
		in r3, [r0, UART_TX_STATUS]
		test r3, 1
		bz test_loop

		ba loop

die:
		mov r1, 1<<4 | 1 | 2
		out [r0, INTERRUPT_ENABLE_PORT], r1

		/* enable interrupts */
		sti

sleep_loop:
		sleep	// sleep
		ba sleep_loop

		ba die
	
banner:
		dw "Hello world! Bootloaded!\r\n"
		dw 0

hs_handler:
		stf r10	// Store flags
		add r7, 1	
		out [r0, GFX_CPR_BGCOL], r7
		mov r9, 0xffff
		out [r0, 0x7001], r9
		rsf r10	// Restore flags
		iret

vs_handler:
		mov r7, r0
		mov r9, 0xffff
		out [r0, 0x7001], r9
		iret

dummy_handler:
		mov r8, '!'	
		out [r0, UART_TX_REG], r8
		mov r9, 0xffff
		out [r0, 0x7001], r9
		iret

